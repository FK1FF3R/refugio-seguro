export class Abrigo {
  constructor({ id, nome, estado, cidade, endereco, telefone, publico, capacidade, tipo }) {
    this.id = id;
    this.nome = nome;
    this.estado = estado;
    this.cidade = cidade;
    this.endereco = endereco;
    this.telefone = telefone;
    this.publico = publico;
    this.capacidade = capacidade;
    this.tipo = tipo;
  }
}
